package edu.opencampus.lms.modelo.aulavirtual.reporte;

public class RecursoNotaParcial {

	private String nota;
	
	private int peso;
	
	private String promedioParcial;

	public RecursoNotaParcial() {
		super();
	}

	public String getNota() {
		return nota;
	}

	public void setNota(String nota) {
		this.nota = nota;
	}

	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}

	public String getPromedioParcial() {
		return promedioParcial;
	}

	public void setPromedioParcial(String promedioParcial) {
		this.promedioParcial = promedioParcial;
	}
	
	
	
}
